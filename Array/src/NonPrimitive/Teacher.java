package NonPrimitive;

public class Teacher {
	private int tId;
	private String tName;
	private double tSal;
	public int gettId() {
		return tId;
	}
	public void settId(int tId) {
		this.tId = tId;
	}
	public String gettName() {
		return tName;
	}
	public void settName(String tName) {
		this.tName = tName;
	}
	public double gettSal() {
		return tSal;
	}
	public void settSal(double tSal) {
		this.tSal = tSal;
	}
	
	
}
